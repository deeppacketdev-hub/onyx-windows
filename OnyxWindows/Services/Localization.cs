    public string t(string key)
    {
        if (_strings.TryGetValue(Language, out var langDict) && langDict.TryGetValue(key, out var val))
        {
            return val;
        }

        // Fallback to English
        if (_strings[AppLanguage.EN].TryGetValue(key, out var fallbackVal))
        {
            return fallbackVal;
        }

        return key;
    }
